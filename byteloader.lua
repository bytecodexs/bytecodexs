repeat
	task.wait()
until game:IsLoaded()
local ByteCode = {}
ByteCode.Universal = "https://raw.githubusercontent.com/bytecodexs/bytecodexs/main/src/byteUniversal.lua"
ByteCode.Games = {}
local function RegisterGame(placeIds, name, url)
	for _, placeId in ipairs(placeIds) do
		ByteCode.Games[placeId] = {
			name = name,
			url = url
		}
	end
end
RegisterGame(
	{
	111385005478215,
	90457367396205
}, "Fish and Monsters!", "https://raw.githubusercontent.com/bytecodexs/bytecodexs/main/src/byteFam.lua")
function ByteCode.Notify(title, text, duration, icon)
	pcall(function()
		game:GetService("StarterGui"):SetCore("SendNotification", {
			Title = title,
			Text = text,
			Icon = icon or "rbxassetid://97024333573219",
			Duration = duration or 5
		})
	end)
end
function ByteCode.Load(url)
	local success, result = pcall(function()
		return loadstring(game:HttpGet(url))()
	end)
	if success then
		ByteCode.Notify("ByteCode", "Successfully loaded script!", 5)
	else
		ByteCode.Notify("ByteCode Error", tostring(result), 8)
	end
	return result
end
local gameData = ByteCode.Games[game.PlaceId]
ByteCode.Notify("ByteCode", "Loading " .. (gameData and gameData.name or "Universal") .. "...", 5)
ByteCode.Load(gameData and gameData.url or ByteCode.Universal)
